    private void jSearchProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchProductButtonActionPerformed
        // This accesses the backend to search for a prompted product:
        if(!ProductList.ReOrderProduct(jReOrderProductComboBox.getSelectedItem().toString(),
                jReOrderProductNameTextField.getText(), jReOrderVendorComboBox.getSelectedItem().toString()))
            jStatusMessageLabel.setText("Product " + jProductNameTextField.getText() + " not found!");
        else{
            ProductList.DisplayProduct(jReOrderProductComboBox.getSelectedItem().toString(),
                    jReOrderProductNameTextField.getText(), jSearchProductTextArea);
            
            jDynamicDeliveryChargeLabel.setText(String.valueOf(ProductList.GetCharges()));
            jDynamicRecommendedAmountLabel.setText(String.valueOf(ProductList.GetRecommendedAmount()));      
        
        try {
              ProductList.DisplayImage(jReOrderImageLabel);
            } catch (IOException ex) {}
        }
    }//GEN-LAST:event_jSearchProductButtonActionPerformed

