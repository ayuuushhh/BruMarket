    private void jLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginButtonActionPerformed
        // This is user login system where a pair of pre-entered data is used to access BruMarket:
        if(Employee.IsEmployee(jUserIDTextField.getText(), jPasswordTextField.getText(), "EmployeeLoginFile.txt")){
            jStatusMessageLabel.setText("Employee " + jUserIDTextField.getText() + " is logged in..."); // Prompts login success
            jMainTabbedPane.addTab("New Product", jTabInMemory5); // Tab manipulation
            jMainTabbedPane.addTab("Re-Order Product", jTabInMemory2);
            jMainTabbedPane.addTab("Product List", jTabInMemory3);
            jMainTabbedPane.addTab("Update Product", jTabInMemory4);
            jMainTabbedPane.setSelectedIndex(1);
            
            if(ProductList.SetReOrder()){
                jStatusMessageLabel.setText("Product: " + ProductList.GetName() + " needs to be Re-Ordered!");
            }
        }else{
            if(Customer.IsCustomer(jUserIDTextField.getText(), jPasswordTextField.getText(), "CustomerLoginFile.txt")){
                jStatusMessageLabel.setText("Customer " + jUserIDTextField.getText() + " is logged in...");
            }else{
                jStatusMessageLabel.setText("Error logging in!");
            }
        }
    }//GEN-LAST:event_jLoginButtonActionPerformed

