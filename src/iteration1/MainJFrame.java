    private void jSaveProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveProductButtonActionPerformed
        // This saves the product into the product list:
        ProductList.GetProduct(jSelectProductComboBox.getSelectedItem().toString(), 
                jProductNameTextField.getText(), Double.valueOf(jProductWeightTextField.getText()),
                Double.valueOf(jNoVATPriceTextField.getText()),Double.valueOf(jNoVATPriceTextField.getText()),
                jExpiryDateTextField.getText(), jDateToOrderTextField.getText(), 
                Double.valueOf(jStockTextField.getText()), Double.valueOf(jMinQuantityTextField.getText()), 
                Double.valueOf(jMaxQuantityTextField.getText()), 
                jImagePathTextField.getText(),
                jVendorComboBox.getSelectedItem().toString());
        
        ProductList.SaveToFile();
        ProductList.DisplayProduct(jSelectProductComboBox.getSelectedItem().toString(), jProductNameTextField.getText(),jNewProductTextArea);
        jStatusMessageLabel.setText("Product " + jProductNameTextField.getText() + " was saved.");
        try {
//          displays the image of the product in the jImageLabel
            ProductList.DisplayImage(jImageLabel);
        } catch (IOException ex) {}
    }//GEN-LAST:event_jSaveProductButtonActionPerformed

